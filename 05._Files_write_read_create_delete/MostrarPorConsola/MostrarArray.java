public class MostrarArray {

    /**
     * Muestra el contenido de un array de Strings
     * @param t 
     */
    public static void mostrarArray (String [] t){
        for  (int i=0 ; i<t.length ; i++){
            System.out.println(t[i]);
        }
    }
}
